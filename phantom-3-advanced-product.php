                            <h4><span class="glyphicon glyphicon-th-list"></span> Produk Detail</h4>
                            <h1>Design And Features</h1>
                            <p>The quadcopter itself is, for the most part, unchanged from its predecessor, the Phantom 2 Vision+. You'll find all the same convenience features like color-coded, self-tightening propellers for easy installation and replacement, and a slot-loading battery pack, though it's a newly designed battery making the Phantom 2's batteries incompatible with the 3.</p>
                            <p>he three-axis gimbal on its belly stabilizes the camera in roll, pitch and yaw directions keeping the video looking smooth even with sudden stick movements or wind gusts. Plus, the camera can do a 90-degree tilt, letting you shoot straight down, straight ahead and anywhere in between. The camera is permanently attached to the gimbal, so if you irreparably damage one or the other, you'll have to replace the entire gimbal-and-camera assembly. It also means you don't have the option of using the camera for anything else, unlike models that use GoPro or other small cameras such as 3D Robotic's Solo or EHang's Ghost.</p>
                            <h1>Flying</h1>
                            <p>If you're reading this review, you probably already know that DJI's Phantom quadcopters have a reputation of being extremely easy to fly (and fly away, but it's something I've never experienced). With the Phantom 3 the experience has only improved.</p>
                            <p>DJI has added a flight simulator to the mobile app (iOS only) that you can use to learn your way around the app and controls. I found it to be a little frustrating because the actual drone performs so much better in real life. Regardless of safety features and how stable the Phantom 3 is, I highly recommend taking your first flights out in an open area where there are no people or distractions</p>
                            <p>Again, if you don't want to use stick commands, you can takeoff and land with a tap and a swipe on your screen. With GPS, the drone will just sit and hover wherever you leave it. Start flying a bit too close to a tree and you can just let go of the sticks and it will stop while you regroup and steer away. You can fly without GPS, but if you release the sticks the Phantom won't stop but instead continues to drift in the direction it was last headed. It's a gut-wrenching experience watching $1,000 float away seemingly uncontrollably, so it's best to fly with GPS until you fully understand the controls.</p>
                            <h1>Video And Photo Quality</h1>
                            <p>Closely examining the 4K video of the Phantom 3 Professional or DJI's higher-end Inspire 1 straight from the camera against the 1080p clips from the Advanced, there are noticeable differences, mainly in the level of fine detail and sharpness of subjects. There is better detail from the 4K and it's especially visible if you're viewing on a large TV or monitor. So, if it's necessary for you to capture the highest quality video without ponying up a ton more cash, the Phantom 3 Professional is the better choice.</p>
                            <p>Frankly, though, the full-HD clips from the Advanced look pretty great as do its photos, and they're more than suitable for sharing online and/or viewing on a phone, tablet or laptop. It's also a huge jump in quality from the Phantom 2 Vision+. For those considering an upgrade from that model to the Professional, your needs might be met with the less expensive Advanced.</p>
