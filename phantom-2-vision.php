                            <h4><span class="glyphicon glyphicon-th-list"></span> Produk Detail</h4>
                            <h1>Phantom 2 Vision</h1>
                            <p>Products such as the DJI Phantom 2 are not only amazing, they change the recreational market because similar devices typically costs thousands of pounds. Not so with the DJI - the price tag is closer within reach of anyone motivated enough to want to save up for one. And it's worth buying because it's so much fun. But then there's the regulation, privacy worries and safety concerns that come with these flying devices. Those are problems that will bother law-makers for a while to come, but we do expect to see some pretty tough legislation being introduced at some point too.</p>
                            <h1>Out Of The Box And Into The sky</h1>
                            <p>The DJI Phantom is a quadcopter. That is exactly what it sounds like - a four rotor arrangement. This is useful because it provides a lot of control, stability and the ability to hover. Because the Phantom 2 Vision is designed to be used as a tool for video, this is very important.</p>
                            <p>We didn't quite know what to expect when it came to setting the DJI Phantom 2 Vision up. But it's fairly straight forward. With the Parrot AR Drone - that we have previously reviewed - it's a case of literally taking it out of the box, installing a battery and that's it. The Phantom is similar, but it requires charging, installing the rotor blades and taking a bit of time out with the instruction manual to make sure everything is covered.</p>
                            <h1>DJI Phone Home</h1>
                            <p>The Phantom 2 is controlled by a traditional-looking radio controller. This has two sticks for control, as well as some other switches for controls that we'll talk about in due course. We never felt out of control with this drone thanks to the control set up, which is more than can be said for the aforementioned Parrot AR.</p>
                            <p>When you first turn the DJI drone on, it needs to get a GPS lock. This is crucial, because it's how the drone will return home safely if something goes wrong. Something going wrong usually means the drone runs low on power, or you send it out of range of the controller. In such cases, the drone should fly to a safe height, return to its launch coordinates and return to the ground. There are some things that can thwart this, but it's much better to have it than not. While we never needed it, we were glad it was built in, because the Phantom is a big and heavy thing at 1.1kgs - so the idea of it going out of range, running out of power the falling to earth is a scary one.</p>
                            <h1>Quadcopter Control</h1>
                            <p>Using the included controller, you can ascend and descend the Phantom 2 by moving the left control up or down, or you can push the stick left or right to get the quad to "yaw" - another way of saying rotate - while maintaining its position while panning. The other stick allows you to fly forward, backward, left and right.</p>
                            <h1>Video And Picture Quality</h1>
                            <p>There are some shortcomings though. Exposure isn't perfect automatically, but you can make adjustments manually from the app on your smartphone, while quality is a touch on the soft side in dimmer lighting conditions. The app's preview quality is only reasonable rather than amazing too, but that's a limitation of signal quality.</p>
                            <p>Another problem is that sometimes a little bit of wind can blow the drone in a way that disrupts the stability of the image. There's no way around this, because you'd need a full gyroscope to stop such movement, and those devices are expensive, heavy and power-hungry. The good news is that we were able to remove a lot of shake by simply running the footage through the Warp Stabilizer built into Adobe Premiere. Most editing software has something similar, as does YouTube these days. This took minor shake out, and gave a really stable image.</p>
                            <p>Still images are also terrific. The 14-megapixel snaps have huge amounts of detail, and with impressive colour balance too. We noticed some image noise and softness when we zoomed into images at 100 per cent scale, but this is a really minor concern.</p>
                            <h1>Battery Life</h1>
                            <p>As promised on the spec sheet, we got about 20-minutes or so from the supplied battery. This is good enough for most people and the ease of swapping out the battery means that you can simply buy more power packs if you want to fly for longer.</p>
                            <p>What this does mean, though, is that you think more about what shots you want - which is a better way to make videos. And while some natural problems with, say, excess wind speed or you messing up the controls might mean that you need to sometimes re-shoot, for the most part the whole system lends itself to getting the shot you need quickly and simply.</p>
                            <h1>More To Come</h1>
                            <p>DJI is also being proactive in adding in future features. For pro users there's a 46mm lens mount kit coming, which will allow photographers to add filters to the camera. That might be very helpful, especially for shooting in bright light when a neutral density (ND) filter might help, or a graduated ND filter to avoid over-exposed skies and underexposed ground.</p>
                            <p>The company is also going to offer native support for DNG format raw file capture in the camera in an update early in 2014, while an additional update will allow waypoint programming. Information is a bit light on this one - it will get announced at the Consumer Electronics Show 2014 in January - but it looks like you'll be able to preset a route for the drone before you set off. Could be very helpful for some users.</p>
