  <h2>Inspire 2</h2>
  <div class="info">Posted on: Jan 20, 2013</div>
  <img src="images/blog/inspire-2.png" class="thumbnail img-responsive"  alt="blog title">
 <p>The Inspire 1 adalah sebuah wahyu. Drone pembuatan film pertama di dunia untuk mengintegrasikan sistem transmisi video HD, gimbal 360 Â° dan kamera 4K,
serta kesederhanaan kontrol aplikasi. Peluncuran kamera Zenmuse X5 dan X5R semakin mengaitkan Inspire sebagai alat penting bagi pembuat film di seluruh dunia.</p>
 <p>The Inspire 2 mengambil semua yang baik tentang Inspire 1 dan meningkatkannya. Sistem pengolah gambar CineCore2.0 telah ditingkatkan ke CineCore2.1,
merekam video hingga 6K di CinemaDNG / RAW dan 5.2K di Apple ProRes bila digunakan dengan kamera Zenmuse X7. Ini berjalan dari 0 hingga 50 mph (80 kpj) hanya dalam 5 detik dan
hits kecepatan maksimum 58 mph (94 kph) dan memiliki kecepatan descent max 9 m / s untuk tidak pernah terdengar kecepatan dan kelincahan dalam pesawat terbang ukuran ini. Sistem baterai ganda
memperpanjang waktu penerbangan hingga maksimum 27 menit (dengan X4S), sementara teknologi pemanasan sendiri memungkinkannya untuk terbang bahkan dalam suhu rendah.
FlightAutonomy telah direvisi dan dikembangkan secara khusus untuk Inspire 2, menyediakan dua arah penghindaran rintangan dan redundansi sensor.
Kecerdasan yang meningkat menambahkan beberapa mode penerbangan cerdas, termasuk Spotlight Pro, memberikan bahkan satu pilot kemampuan untuk membuat gambar dramatis yang kompleks.
Sistem transmisi video yang sudah ditingkatkan sekarang mampu memiliki frekuensi sinyal ganda dan saluran ganda, streaming video dari FPV onboard
kamera dan kamera utama secara bersamaan, untuk kolaborasi operator dan pilot kamera yang lebih baik.</p>
